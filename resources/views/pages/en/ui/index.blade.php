<x-page title="Components">

<x-p>
    To create the <strong>MoonShine</strong> admin panel interface, many <em>UI components</em> are used.<br />
    You can use these components to build your templates,
    custom fields, filters, decorations or pages.<br />
    The use of components speeds up development and allows you to maintain a unified style of the project.
</x-p>

<x-p>
    If you plan to use components outside of the admin panel,
    then you need to add a directive with MoonShine assets to your template.
</x-p>

<x-code language="blade">
    @@moonShineAssets
</x-code>

